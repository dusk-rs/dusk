package rs.dusk.engine.entity.character.player.social

object Duration {

    const val MICROSECOND = 1000L
    const val MILLISECOND = 1000L * MICROSECOND
    const val SECOND = 1000L * MILLISECOND
    const val MINUTE = 60L * SECOND
    const val HOUR: Long = 60L * MINUTE
    const val DAY: Long = 24L * HOUR

}